
function isNumberBiggerThan100(a){
    if(a > 100){
        console.log(`Number a: ${a} is bigger than 100`);
    }
    else if(a < 100){
        console.log(`Number a: ${a} is less than 100`);
    }
    else{
        console.log(`Number a: ${a} is equal to 100`);
    }
}
isNumberBiggerThan100(100);